// objetos
var obj = {
    nome: "Mesaque",
    idade: 18,
    profissão: "Programador"
};

console.log(obj);
console.log(typeof obj);

// Como acessar uma propriedade no objeto.
console.log(obj.nome);
console.log(obj.idade);
console.log(obj.profissão);

// Podemos também usar como se fosse uma variavel como no exemplo abaixo, é so concatenar
console.log("O meu nome é " + obj.nome);
console.log("Eu tenho:" + obj.idade);
console.log("Eu sou um:" + obj.profissão);